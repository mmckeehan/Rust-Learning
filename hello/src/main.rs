#![allow(unused_variables)]

//Create constants by using "const"
const CATS: i32= 999;

fn main() {
    println!("Hello, world!");
    // "let" sets the variable to a word
    // Add "mut" before the variable to make it mutable
    let bunnies = 16;
    let mut dogs = 2;
    dogs = 4;



    println!("{}, {}, {}", bunnies, dogs, CATS);
}